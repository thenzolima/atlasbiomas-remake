                                    <p class="mb-4"><strong>Imagem 1: </strong>Borboleta da espécie <i>Caligo illioneus</i>. popularmente conhecida como Borboleta-coruja, é uma das maiores borboletas do mundo, as fêmeas são maiores e menos coloridas que os machos, podem chegar a apresentar 18 cm de envergadura. O interior das asas desses animais se assemelha a face de uma coruja, e é importante na proteção do inseto contra predadores. Muitas vezes a Borboleta Coruja é confundida por outros animais com um bicho mais perigoso. Região: Borboletário Asas Mágicas, unidade de conservação no município de Porto Seguro, Bahia.<br/><strong>Imagem 2: </strong>Borboleta da espécie Caligo illioneus, pertencente a Família: Ninfalídeos/Morphinae. Tem esse nome, pois a faca externa das asas tem um desenho semelhante ao rosto de uma coruja, ou serpente com destaque para os olhos enormes e abertos. É possível que esta estampa sirva de maneira eficiente para driblar seus predadores. De hábitos crepusculares quando adulta (voa lentamente ao amanhecer e ao anoitecer), ela é um dos maiores exemplares de borboletas que se tem notícia (chega a medir até 18 centímetros de envergadura). Vive de 3 a 4 meses. Metamorfose leva cerca de 105 dias. Em geral as fêmeas são maiores e menos coloridas que os machos. Nenhum exemplar é igual ao outro, é como uma impressão digital. Um de seus principais inimigos é a vespa, que coloca seus ovos na lagarta da borboleta-coruja de qualquer sexo. Inseticidas usados nas plantações de banana também costumam causar sua morte. Soma-se a isso, está ameaçada por captura criminosa e pela destruição de seu habitat. Região: Borboletário Asas Mágicas, unidade de conservação do município de Porto Seguro, Bahia.</p>

                          

